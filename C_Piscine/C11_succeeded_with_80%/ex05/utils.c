#include "ex05.h"

void	ft_putnbr(int nbr)
{
	char	ch;

	if (nbr == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nbr < 0)
	{
		write(1, "-", 1);
		nbr = -nbr;
	}
	if (nbr >= 10)
		ft_putnbr(nbr / 10);
	ch = nbr % 10 + '0';
	write(1, &ch, 1);
}
